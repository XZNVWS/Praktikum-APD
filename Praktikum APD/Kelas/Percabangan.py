umur = int(input("Masukkan umur Anda :"))
           
if umur <= 10 : print("Umur Anda termasuk kategori anak-anak")

elif umur <= 18 : print("Umur Anda termasuk kategori remaja")

elif umur <= 35 : print("Umur Anda termasuk kategori dewasa")

elif umur <= 65 : print("Umur Anda termasuk kategori paruh baya")

else : print("Umur Anda termasuk kategori Tua")